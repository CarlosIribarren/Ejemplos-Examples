package pruebas;

public class Persona {

  private Integer codigo;
  private String nombre;

  public Persona() {
  }

  public Integer getCodigo() {
    return this.codigo;
  }

  public void setCodigo(final Integer codigo) {
    this.codigo = codigo;
  }

  public String getNombre() {
    return this.nombre;
  }

  public void setNombre(final String nombre) {
    this.nombre = nombre;
  }

}
