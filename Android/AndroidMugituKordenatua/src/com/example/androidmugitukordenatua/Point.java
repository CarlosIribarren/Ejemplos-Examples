package com.example.androidmugitukordenatua;

public class Point {
	
	private Double lat;
	private Double lon;
	
	public Point()
	{
		
	}
	
	public Point( Double latitudea, Double longitudea)
	{
		latitudea = lat;
		longitudea = lon;
	}
	
	public Double getLat() {
		return lat;
	}
	public void setLat(Double lat) {
		this.lat = lat;
	}
	public Double getLon() {
		return lon;
	}
	public void setLon(Double lon) {
		this.lon = lon;
	}
	
	

}
